namespace MarkMello.Domain;

/// <summary>
/// Режим отображения миникарты документа в viewer mode.
/// </summary>
public enum DocumentMinimapMode
{
    /// <summary>
    /// Показывать миникарту только когда она уместна для чтения: широкий экран,
    /// длинный документ и доступная прокрутка.
    /// </summary>
    Auto = 0,

    /// <summary>
    /// Показывать миникарту всегда, когда документ прокручивается и может быть
    /// безопасно отрисован в миниатюру.
    /// </summary>
    On = 1,

    /// <summary>
    /// Никогда не показывать миникарту документа.
    /// </summary>
    Off = 2
}
